package com.github.mouse0w0.peach.mcmod;

public class GameData {
    private final String id;
    private final String name;

    public GameData(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
